class Operations {

    int addition(int fNum, int sNum) {
        return fNum + sNum;
    }
    int subtraction(int fNum, int sNum) {
        return fNum - sNum;
    }
    int multiplication(int fNum, int sNum) {
        return fNum * sNum;
    }
    int dividing(int fNum, int sNum) {
        return fNum / sNum;
    }
}
